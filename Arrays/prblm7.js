// Updating an Array

var superheroes = [];

superheroes.push("Iron Man");
superheroes.push("Ant Man");
superheroes.push("Dr Strange");
console.log(superheroes);

console.log("----------------------Before-------------------------");

console.log(superheroes[0]);
console.log(superheroes[1]);
console.log(superheroes[2]);


console.log("----------------------After-------------------------");


superheroes[0] = "Thor";

console.log(superheroes[0]);
console.log(superheroes[1]);
console.log(superheroes[2]);
