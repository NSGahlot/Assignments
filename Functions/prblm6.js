{
    
    var sum = x+y;
    return sum;
}

function subtraction(x, y)
{
     
     var sub = x-y;
     return sub;
}

function multiplication(x, y) 
{
    var multi = x*y;
    return multi;
}

function divison(x, y)
{
    var div = x/y;
    return div;
}

